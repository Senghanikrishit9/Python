# 1. Calculate grade from marks using if-else

marks = int(input("enter your marks :-"))

if marks >= 90:
    print("you got A+ grade")
elif marks >= 80 :
    print("you got A grade")
elif marks >= 70 :
    print("you got B+ grade")
elif marks >= 60 :
    print("you got B grade")
elif marks >= 50 :
    print("you got C grade")
elif marks >= 40 :
    print("you got C+ grade")
else :
    print("you are fail")
